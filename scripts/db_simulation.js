function addTable(name)
{
    let id          = -1;
    let isNameUnq   = isTableNameUnique(name);

    if (isNameUnq)
    {
        let id = generateUniqueId();
        tables.set(id, name);
        storeTables();
        storeLastId();
    }

    return [id, isNameUnq];
}



function isTableNameUnique(name)
{
    for (let tabName of tables.values())
    {
        if (tabName === name)
        {
            return false;
        }
    }

    return true;
}



function deleteTable(name)
{
    for (let idName of tables.entries())
    {
        if (idName[1] === name)
        {
            tables.delete(idName[0]);
            storeTables();
            storeLastId();
            return true;
        }
    }

    return false;
}



function getTableId(tabName)
{
    for (let idName of tables.entries())
    {
        if (idName[1] === tabName)
        {
            return idName[0];
        }
    }

    return -1;
}



function getAllTables()
{
    return tables;
}



// categories ////////////////////////////////////////////////////////////////////////////////



function addCategoryDBOnly(tableId, name)
{
    let id = generateUniqueId();

    let newCategory = {
        id          : id,
        tableId     : tableId,
        name        : name,
        color       : getRandomColor(),
        isSelected  : false
    };

    categories.push(newCategory);
    storeCategories();
    storeLastId();
}



function addCategory(tableId, name)
{
    let id = addCategoryDBOnly(tableId, name);
    addResource(categories[categories.length - 1]);

    return id;
}



function deleteCategory(categoryId)
{
    categories = categories.filter(category => category.id !== categoryId);

    storeCategories();
    storeLastId();
}



function getAllCategories()
{
    return categories;
}



function isCategoryNameUniqueInTable(tableId, categoryName)
{
    let categoriesWithTheSameName = categories.filter(category =>
        category.tableId === tableId &&
        category.name === categoryName);

    return categoriesWithTheSameName.length === 0;
}



