function addTaskDBOnly(tableId, title, description, start, end, hashTags, category)
{
    let id = generateUniqueId();

    let newTask = {
        id          : id,
        tableId     : tableId,
        title       : title,
        description : description,
        start       : start,
        end         : end,
        hashTags    : hashTags,
        category    : category
    };

    tasks.push(newTask);

    storeTasks();
    storeLastId();
}


